package fonts

type Font8x8 struct {
	Height int
	Width  int
	Bitmap [][]byte
}

func (f *Font8x8) Load() {
	f.Height = 8
	f.Width = 8
	f.Bitmap = [][]byte{
		// A
		{
			0x7E, // 01111110
			0x42, // 01000010
			0x42, // 01000010
			0x7E, // 01111110
			0x42, // 01000010
			0x42, // 01000010
			0x42, // 01000010
			0x00, // 00000000
		},
		// B
		{
			0x7C, // 01111100
			0x42, // 01000010
			0x42, // 01000010
			0x7C, // 01111100
			0x42, // 01000010
			0x42, // 01000010
			0x7C, // 01111100
			0x00, // 00000000
		},
		// C
		{
			0x7E, // 01111110
			0x40, // 01000000
			0x40, // 01000000
			0x40, // 01000000
			0x40, // 01000000
			0x40, // 01000000
			0x7E, // 01111110
			0x00, // 00000000
		},
	}
}
